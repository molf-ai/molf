/** Create a small valid PNG (1x1 red pixel) as a base64 string. */
export function createTestPngBase64(): string {
  return Buffer.from(pngBytes).toString("base64");
}

/** Create a small valid PNG (1x1 red pixel) as a File object. */
export function createTestPngFile(filename = "test.png"): File {
  return new File([pngBytes], filename, { type: "image/png" });
}
